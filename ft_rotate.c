#include "push_swap.h"

static void	rotate(t_stack **stack)
{
	t_stack	*tmp;
	t_stack	*last_node;

	if (stack == NULL || *stack == NULL)
		return ;
	tmp = *stack;
	tmp->next = NULL;
	*stack = (*stack)->next;
	last_node = find_last_node(*stack);
	last_node->next = tmp;
}

void	ra(t_stack **a)
{
	rotate(a);
	write(1, "ra\n", 3);
}

void	rb(t_stack **b)
{
	rotate(b);
	write(1, "rb\n", 3);
}

void	rr(t_stack **a, t_stack **b)
{
	rotate(a);
	rotate(b);
	write(1, "rr\n", 3);
}
